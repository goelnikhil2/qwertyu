package sample_pkg;

public class datatype {
	public static void main(String[] args) {
		System.out.println("floatdoubleint!!");
		
		int a = 3;
		float b = 5f / 3f;
		double c= 7d / 6d;
		
		System.out.println("int = "  +a);
		System.out.println("float = " +b);
		System.out.println("double = " +c);
		
		double d= 9d;
		double e= d * .498d;
		System.out.println("double e= " +e);
		
	}
}
